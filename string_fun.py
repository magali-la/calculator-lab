# bonus strings practice in python - script takes favorite word
user_input = input("Share your favorite word: ")
print(f"Your favorite word is: {user_input}")

# length of word
print(f"Your favorite word is {len(user_input)} letters long")
# word in caps
print(f"Your favorite word in CAPS: {user_input.upper()}")
# word 3 times
print(f"Your favorite word 3 times: {user_input * 3}")